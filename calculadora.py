num1 = 10
num2 = 8

def suma(numero1, numero2):
    total = numero1 + numero2
    return total

def resta(numero1, numero2):
    total = numero1 - numero2
    return total



resultado = suma(num1, num2)
resultado2 = resta(num1,num2)
print('El valor de la suma es ' ,resultado)
print('El valor de la resta es ' ,resultado2)

